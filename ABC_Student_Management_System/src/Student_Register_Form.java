    private void save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_btnActionPerformed
        
        PreparedStatement ps;
        
        String id = id_txt.getText();
        String name = name_txt.getText();
        String address = address_txt.getText();
        String gender;
        if (male_radio.isSelected()) {
            gender="Male";
        }else if(female_radio.isSelected()){
            gender="Female";
        }else{
            gender = "No Selected";
        } 
        String qualification;
        if (ol_check.isSelected()) {
            qualification = "O/L";
        }else if (al_check.isSelected()) {
            qualification = "A/L";
        }else if (nvq_check.isSelected()) {
            qualification = "NVQ-4";
        }else{
            qualification = "Nothing Added..";
        }
        String course = (String) cource_combo.getSelectedItem();
        String dob = dob_txt.getText();
        
        
        String insert_query = "INSERT INTO `student`(`sid`, `name`, `address`, `gender`, `qualification`, `course`, `dob`) VALUES (?,?,?,?,?,?,?)";
        
        try {
            ps = My_Connection.getConnection().prepareStatement(insert_query);
            ps.setString(1, id);
            ps.setString(2, name);
            ps.setString(3, address);
            ps.setString(4, gender);
            ps.setString(5, qualification);
            ps.setString(6, course);
            ps.setString(7, dob);
            
            ps.executeUpdate();
            table_view();
            JOptionPane.showMessageDialog(null, "Value is added...");
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Student_Register_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_save_btnActionPerformed

