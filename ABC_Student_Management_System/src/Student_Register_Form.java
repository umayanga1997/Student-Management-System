    private void search_txtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_search_txtKeyReleased
       
        String text = search_txt.getText();
        String tx= "";
        if (text.equals(tx)) {
            clearTextMethod();
            table_view();
        } else {

            seachingMethod();
            search_tableMethod();
        }
        
        
    }//GEN-LAST:event_search_txtKeyReleased

    public void search_tableMethod(){

        PreparedStatement ps;
        ResultSet rs;
   
        try {
            
            String query ="SELECT * FROM `student` WHERE `sid`=?";
            ps = My_Connection.getConnection().prepareStatement(query);
            ps.setString(1, search_txt.getText());
            rs = ps.executeQuery();
            detail_tble.setModel(DbUtils.resultSetToTableModel(rs));
 
        } catch (Exception e) {
        }
}
    
    public void seachingMethod(){
    
        PreparedStatement ps;
        ResultSet rs;
  
        String male = "Male";
        String female = "Female";
        String ol = "O/L";
        String al = "A/L";
        String nvq = "NVQ-4";
        
        
        String searching_value = search_txt.getText();
   
        String search_query ="SELECT * FROM `student` WHERE `sid`=?";
        
        try {
            
            
            ps = My_Connection.getConnection().prepareStatement(search_query);
            ps.setString(1, searching_value);
            rs = ps.executeQuery();
  
            if (rs.next()) {
                 
                id_txt.setText(rs.getString("sid"));
                name_txt.setText(rs.getString("name"));
                address_txt.setText(rs.getString("address"));
                
                if (male.equals(rs.getString("gender"))) {
                    male_radio.setSelected(true);
                    female_radio.setSelected(false);
                }else if (female.equals(rs.getString("gender"))){
                    female_radio.setSelected(true);
                    male_radio.setSelected(false);
                }else if (!male.equals(rs.getString("gender")) || !female.equals(rs.getString("gender"))) {
                    female_radio.setSelected(false);
                    male_radio.setSelected(false);
                }
                
                cource_combo.setSelectedItem(rs.getString("course"));
                dob_txt.setText(rs.getString("dob"));
                
                if (ol.equals(rs.getString("qualification"))) {
                    ol_check.setSelected(true);
                    al_check.setSelected(false);
                    nvq_check.setSelected(false);
                }else if (al.equals(rs.getString("qualification"))) {
                    al_check.setSelected(true);
                    ol_check.setSelected(false);
                    nvq_check.setSelected(false);
                }else if (nvq.equals(rs.getString("qualification"))) {
                    nvq_check.setSelected(true);
                    ol_check.setSelected(false);
                    al_check.setSelected(false);
                }
                
            }

        } catch (Exception e) {
        }
}
